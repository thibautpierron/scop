#include "libft.h"

int		ft_atoi(const char *str)
{
	int i;
	int res;
	int signe;

	res = 0;
	i = 0;
	signe = 1;
	while (str[i] == ' ' || str[i] == '\n' || str[i] == '\t' ||\
str[i] == '\r' || str[i] == '\v' || str[i] == '\f')
		i++;
	if (str[i] == '+')
		i++;
	else if (str[i] == '-')
	{
		signe = -1;
		i++;
	}
	while (ft_isdigit(str[i]))
	{
		res = res * 10 + str[i] - '0';
		i++;
	}
	return (res * signe);
}
